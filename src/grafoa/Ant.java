public class Ant {
    private int pasos;
    private int cantidad;

    public Ant(int pasos, int cantidad) {
        this.pasos = 0;
        this.cantidad = 0;
    }

    public int getPasos() {
        return pasos;
    }

    public void setPasos(int pasos) {
        this.pasos = pasos;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }
    
    
}
